# Swapping Variables

# Circle 
pi = 3.14
r = 2
circle = 2 * pi *r

# Random Variables
a = 3
b = 5



print("Circle = ", circle,"\n a is ", a, "\n b is ", b)

temp = a
a = b

b = temp

print("\n a ", a , "\n b is ", b )

